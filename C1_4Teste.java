public class C1_4Teste {
    public static void main(String[] args) {
        int a = 100;
       
        if (a == 10) {
            // SE for verdadeiro
            System.out.println("Valor de a é 10");
        } else if (a == 20) {
            // SENAO SE for verdadeiro
            System.out.println("Valor de a é  20");
        } else if (a == 100) {
            // SENAO SE for verdadeiro
            System.out.println("Valor de a é 100");
        } else {
            // SENAO nenhuma é verdadeira
            System.out.println("Nenhuma condicao eh verdadeira");
        }
    }
 }
 
